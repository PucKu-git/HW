/*
Задача 13: Напишите программу, которая выводит третью цифру заданного числа или сообщает,
что третьей цифры нет.
645 -> 5
78 -> третьей цифры нет
32679 -> 6
*/

int ConsoleReading (string message)
{
    Console.Write(message);
    int number = int.Parse(Console.ReadLine()!);
    return number;
}

// Определяем метод ShowThirdDigit, который принимает число и выводит его третью цифру или сообщение об отсутствии
void ShowThirdDigit(int number)
{
// Проверяем, есть ли у числа третья цифра, то есть оно больше или равно 100
    if (number >= 100)
    {
// Используем оператор / (целочисленное деление), чтобы получить третью цифру числа с начала
// Для этого сначала делим число на 10 в степени n-3, где n - количество цифр в числе,
// чтобы отбросить последние n-3 цифры,
// а затем берем остаток от деления на 10, чтобы получить третью цифру с начала
        int n = (int)Math.Log10(number) + 1; // вычисляем количество цифр в числе
        int thirdDigit = (number / (int)Math.Pow(10, n - 3)) % 10; // вычисляем третью цифру с начала

// Выводим результат на консоль
        Console.WriteLine("Третья цифра числа {0} равна {1}", number, thirdDigit);
    }
    else
    {
// Выводим сообщение об отсутствии третьей цифры
        Console.WriteLine("У числа {0} нет третьей цифры", number);
    }
}

// Считываем число с консоли
int number = ConsoleReading ("Введите число: ");
// Вызываем метод ShowThirdDigit, который принимает число и выводит его третью цифру или сообщение об отсутствии
ShowThirdDigit(number);